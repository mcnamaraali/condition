
public class Condition {
	public static void main(String[] args) {
		
		//postfix decrement operator 
		
		int c = 5; //declaring and assigning the variable c 
		System.out.printf("c before postincrementation: %d%n", c);
		System.out.printf("c postincrementation: %d%n", c--);
		System.out.printf("c after postincrementation: %d%n", c);
		
		System.out.println();
		
		c = 5;
		//prefix decrement operator
		
		System.out.printf("c before postincrementation: %d %n", c);
		System.out.printf("c before postincrementation: %d %n", --c);
		System.out.printf("c before postincrementation: %d %n", c);
	
	}

}
